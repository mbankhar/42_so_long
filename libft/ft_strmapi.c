#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		counter;
	char	*new_string;
	int		i;

	i = 0;
	counter = ft_strlen(s);
	new_string = (char *)malloc(sizeof(char) * (counter + 1));
	if (new_string == NULL)
		return (NULL);
	while (s[i])
	{
		new_string[i] = f(i, s[i]);
		i++;
	}
	new_string[i] = '\0';
	return (new_string);
}
